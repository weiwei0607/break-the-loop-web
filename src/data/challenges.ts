const createChallenges = (
  tasks: string[],
  difficulty: 'easy' | 'medium' | 'hard'
): Challenge[] => {
  return tasks.map((text, index) => ({
    id: `${difficulty}-${index}`,
    text,
    difficulty,
  }));
};

